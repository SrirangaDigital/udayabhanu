			<h2 class="text-center my-3">ನಮ್ಮ ಪರಿಚಯ</h2>
			<p>ಮಹಾಮಹೋಪಾಧ್ಯಾಯ ವಿದ್ವಾನ್. 
			ಎನ್ ರಂಗನಾಥಶರ್ಮಾರವರ ಬದುಕು ಹಾಗೂ ಬರಹಗಳನ್ನು ಪ್ರಪಂಚಾದ್ಯಂತ ತಲುಪಿಸುವ ಉದ್ದೇಶದಿಂದ ಮಹಾಮಹೋಪಾಧ್ಯಾಯ ವಿದ್ವಾನ್  ಎನ್ ರಂಗನಾಥಶರ್ಮಾ ಶೋಧ ಕೇಂದ್ರ ಎಂಬ ಸಂಶೋಧನಾ ಕೇಂದ್ರವನ್ನು ಆರಂಭಿಸಿ ಇದರ ಮೂಲಕ ಶ್ರೀಯುತ ಶರ್ಮರ ಸಾಹಿತ್ಯಗಳ ಬಗ್ಗೆ ವಿಶೇಷಾಧ್ಯಯನ  ಅಥವಾ ಸಂಶೋಧನೆ ಮಾಡುವವರಿಗೆ ಅನುಕೂಲವಾಗಲೆಂಬ ಉದ್ದೇಶದಿಂದ ಅವರ <a href="<?=DOWNLOAD_URL?>orderCopy.pdf" target="_blank">ಆದೇಶದಂತೆ</a> ಈ ತಾಣದಲ್ಲಿ ಅವರ ಸಮಗ್ರ ಪರಿಚಯ ಹಾಗೂ ಸಾಹಿತ್ಯವನ್ನು ನೀಡಿದ್ದೇವೆ. ತಾಣದಲ್ಲಿ ಲಭ್ಯವಿರುವ ಪುಸ್ತಕಗಳನ್ನು ಅಧ್ಯಯನ ಹಾಗೂ ಸಂಶೋಧನೆಗೆ ಮಾತ್ರ ಉಪಯೋಗಿಸಬೇಕೇ ಹೊರತು ಯಾರೂ ಕೂಡ ಯಾವುದೇ ರೀತಿಯಾದ ವಾಣಿಜ್ಯ ಉದ್ದೇಶದಿಂದ ಬಳಸುವಂತಿಲ್ಲ.</p>
			<p>ಅನೇಕ ಮಠ, ಮಂದಿರಗಳು, ಪ್ರಕಾಶಕರು ಶ್ರೀಯುತ ಶರ್ಮರ ಮೇಲಿನ ಗೌರವ ಹಾಗೂ ಸಾಹಿತ್ಯದ ಮೇಲಿನ ಪ್ರೀತಿಯಿಂದ ಮುದ್ರಿಸಿ ಯಾವುದೇ ವಾಣಿಜ್ಯ ಉದ್ದೇಶವನ್ನು ಹೊಂದದೆ ಕಡಿಮೆ ಬೆಲೆಯಲ್ಲಿ ಜನರಿಗೆ ತಲುಪಿಸಿ ಅವರ ಸಾಹಿತ್ಯವನ್ನು ಸಂರಕ್ಷಣೆ ಮಾಡಿದ್ದಾರೆ. ಇಂಥಹ ಬಹುಶ್ರಮಸಾಧ್ಯವಾದ ಮಹತ್ಕಾರ್ಯವನ್ನು ಮಾಡಿದ ಎಲ್ಲಾ ಪ್ರಕಾಶಕರನ್ನು ಈ ಸಂದರ್ಭದಲ್ಲಿ ಕೃತಜ್ಞತಾಪೂರ್ವಕವಾಗಿ ಸ್ಮರಿಸುತ್ತೇವೆ. </p>
			<p>ಈ ಎಲ್ಲಾ ಗ್ರಂಥಗಳ ಲಭ್ಯತೆ, ಬೆಲೆ, ಪುಟಗಳ ಸಂಖ್ಯೆ, ಪ್ರಕಾಶಕರು ಇತ್ಯಾದಿ ವಿವರಗಳನ್ನು ನೀಡಲಾಗಿದೆ. ಆಸಕ್ತರು ಪ್ರಕಾಶಕರನ್ನು ನೇರವಾಗಿ ಸಂಪರ್ಕಿಸಿ ಪ್ರತಿಗಳನ್ನು ಖರೀದಿಸಬಹುದು. ಗ್ರಾಹಕರ ಅನುಕೂಲಕ್ಕಾಗಿ ಗ್ರಂಥದಲ್ಲಿ ಲಭ್ಯವಿರುವಂತೆ ಪ್ರಕಾಶಕರ ವಿಳಾಸ ಹಾಗೂ ದೂರವಾಣಿ ಸಂಖ್ಯೆಯನ್ನೂ ನೀಡಲಾಗಿದೆ.</p>
			<p>ಪೂಜ್ಯ ಶರ್ಮರ ಅಪೇಕ್ಷೆಯನ್ನು ಪೂರೈಸುವ ಉದ್ದೇಶದಿಂದ ಈ ವೆಬ್ಸೈಟ್ ನಲ್ಲಿ ಶರ್ಮರ ಸಮಗ್ರ ಸಾಹಿತ್ಯವನ್ನು ಪ್ರಕಟಿಸುವ ಕಾರ್ಯಕ್ಕೆ ಡಾ.ಶಾರದಾಚೈತ್ರರವರು ಅನುಮತಿ ನೀಡಿ ಸರ್ವರೀತಿಯಲ್ಲೂ ಸಹಕರಿಸಿದ್ದಾರೆ.  ಇವರು ಪೂಜ್ಯ ಶರ್ಮರ ಎಲ್ಲಾ ಗ್ರಂಥಗಳನ್ನು ಇ-ಪುಸ್ತಕಗಳಾಗಿ <span class="english">Google Play Books / Kindle / Amazon</span> ಮುಂತಾದ ಸೌಲಭ್ಯಗಳನ್ನುಪಯೋಗಿಸಿ ಸ್ಮಾರ್ಟ್ ಫೋನ್ ಗಳಲ್ಲಿ ಓದಲು ಅನುಕೂಲವಾಗುವಂಥಹ ಮಹತ್ತರವಾದ ಕಾರ್ಯವನ್ನು ಮಾಡಿಸುತ್ತಿದ್ದಾರೆ. ಸದ್ಯದಲ್ಲೇ ಸಾಹಿತ್ಯಾಸಕ್ತರಿಗೆ ಹಾಗೂ ಅಭಿಮಾನಿಗಳಿಗೆ ಶರ್ಮರ ಸಮಗ್ರ ಸಾಹಿತ್ಯಗಳು ಇನ್ನೂ ಸುಲಭವಾಗಿ ದೊರಕಲಿವೆ. ವಿದ್ವಾನ್ ಎನ್. ರಂಗನಾಥಶರ್ಮರ ಗ್ರಂಥಗಳನ್ನು ಪುನರ್ಮುದ್ರಣ ಮಾಡಲಿಚ್ಛಿಸುವವರು <span class="english">schaitra2007@hotmail.com</span> ಈಮೈಲ್ ಮೂಲಕ ಅವರನ್ನು ಸಂಪರ್ಕಿಸಬಹುದು. ಶರ್ಮರ ಅಪೇಕ್ಷೆಯನ್ನು ಪೂರೈಸುವಲ್ಲಿ ಸಹಕರಿಸಿದ ಇವರಿಗೆ ವಿಶೇಷವಾದಕೃತಜ್ಞತೆಗಳನ್ನು ಸಲ್ಲಿಸುತ್ತೇವೆ.</p>
			<p>ಶ್ರೀಯುತರ ಸರ್ವತೋಮುಖವಾದ 
			ಪರಿಚಯ ಮಾಡಿಕೊಡುವ 
			ಉದ್ದೇಶದಿಂದ ಅವರ ಬಗ್ಗೆ ಶಿಷ್ಯರು, ಮಕ್ಕಳು, ಹಾಗೂ ಅಭಿಮಾನಿಗಳು ಬರೆದ ಪರಿಚಯ ಗ್ರಂಥಗಳನ್ನೂ, ಲೇಖನಗಳನ್ನೂ, ವಾರ್ತಾಪತ್ರಿಕೆ ಮಾಹಿತಿಗಳನ್ನೂ ನೀಡಲಾಗಿದೆ. </p>
			<p>ಈ ತಾಣವನ್ನು ಇನ್ನೂ ಉತ್ತಮಗೊಳಿಸುವ ನಿಟ್ಟಿನಲ್ಲಿ ಸಹೃದಯರ ಪ್ರಾಮಾಣಿಕ ಸಲಹೆ ಸೂಚನೆಗಳಿಗೆ ಸದಾ ಸ್ವಾಗತ. </p>
