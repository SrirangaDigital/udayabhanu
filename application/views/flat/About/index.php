						<h4>सारस्वतोपासनम्।</h4>
						<p>छात्रस्तरादारभ्यापि श्रीरङ्गनाथशर्मणां लेखने महती आसक्तिः आसीत्। पद्यरचना, चर्चास्पर्धा, भाषणस्पर्धा इत्यादिषु शर्माणः सोत्साहं भागम् ऊढवन्तः। बाल्यादपि पुस्तकप्रीतिः आसीत्, वृत्तिजीवने तु एकैकं क्षणमपि अध्यनेन अध्यापनेन वा यापितवन्तः शर्माणः। उपन्यासः, नाटकादिषु भागवहनञ्च तेषां प्रवृत्तिः आसीत्। पठ्यरचनासमितिषु शर्मणां योगदानम् अमूल्यमासीत्। श्रीयुतानां डि.वि.जि वर्याणां प्रेरणया समग्रस्य श्रीमद्वाल्मीकिरामायणस्य कन्नडानुवादावकाशः शर्मभिः प्राप्तः। इदं कन्नडजनानां महत्सौभाग्यमेव। कन्नडभाषामर्यादां, सौन्दर्यं च मनसि निधाय शर्माणः अत्युत्कृष्टभाषया रामायणमनूद्य कन्नडजनानां रामरसायनास्वादने उपकृतवन्तः। अपि च कन्नडसाहित्यपरिषदः पुरातनकन्नडगद्यानुवादसमितेः प्रधानसंपादकाः भूत्वा, प्रायः त्रयोदश ग्रन्थान् आधुनिककन्नडभाषया अनूदितवन्तः। नैकासु दिनपत्रिकासु, साप्ताहिकपत्रिकासु, मासपत्रिकासु च लेखनानि विलिख्य निरन्तरं स्वचिन्तनानि समाजे प्रसारितवन्तः। संस्कृतलेखनानि, नाटकानि, स्तोत्राणि, कन्नडग्रन्थाः, अनुवादग्रन्थाः इत्येवं संस्कृतकन्नडसाहित्यक्षेत्रे नवतेः अधिकान् ग्रन्थान् विरचय्य महतीं सारस्वतसेवां विधाय स्वजीवनसार्थक्यं प्राप्तवन्तः। सर्वत्र विख्यातासु विद्वद्गोष्ठीषु भागमूढ्वा सर्वदा सरस्वतीसेवायां निरताः आसन्। जातिमतादीनां भेदं विना सर्वेषु मठेषु प्रचलितासु गोष्ठीषु भागमूढवन्तः।</p>
						<h4>प्रशस्तयः।</h4>
