						<h4>ಸಾರಸ್ವತೋಪಾಸನೆ</h4>
						<p>ವಿದ್ಯಾರ್ಥಿ ಜೀವನದಿಂದಲೇ ಶ್ರೀಯುತರಿಗೆ ಬರವಣಿಗೆಯೆಂದರೆ ಅತೀವ ಆಸಕ್ತಿ. ಕವಿತಾ ರಚನೆ, ಚರ್ಚಾಸ್ಪರ್ಧೆ, ಭಾಷಣ ಸ್ಪರ್ಧೆಗಳಲ್ಲಿ ಭಾಗವಹಿಸುವುದೆಂದರೆ ಶ್ರೀಯುತರಿಗೆ ಅಪರಿಮಿತೋತ್ಸಾಹ. ಬಾಲ್ಯದಿಂದಲೇ ಪುಸ್ತಕದ ಹುಳು ಎನಿಸಿಕೊಂಡಿದ್ದ ಶ್ರೀಯುತರು ವೃತ್ತಿಜೀವನದಲ್ಲಂತೂ ಪ್ರತಿಕ್ಷಣವನ್ನೂ ಅಧ್ಯಯನ ಅಥವಾ ಅಧ್ಯಾಪನಕ್ಕೆ ವಿನಿಯೋಗಿಸುತ್ತಿದ್ದರು. ಉಪನ್ಯಾಸ, ತಾಳಮದ್ದಲೆ - ನಾಟಕಗಳಲ್ಲಿ ಭಾಗವಹಿಸುವುದು ಇವರ ಹವ್ಯಾಸವಾಗಿತ್ತು. ಪಠ್ಯರಚನಾ ಸಮಿತಿಯಲ್ಲಿ ಇವರ ಸೇವೆ ಅಪಾರ. ಶ್ರೀಯುತ ಡಿ.ವಿ.ಜಿ.ಯವರ ಪ್ರೇರಣೆಯಿಂದ ಸಮಗ್ರ ಶ್ರೀಮದ್ವಾಲ್ಮೀಕಿರಾಮಾಯಣವನ್ನು ಕನ್ನಡಕ್ಕನುವಾದಿಸುವ ಭಾಗ್ಯ ಇವರಿಗೊದಗಿ ಬಂತು. ಇದು ಕನ್ನಡ ನಾಡಿದ ಜನತೆಯ ಮಹಾಸೌಭಾಗ್ಯವೇ ಆಯಿತು. ಕನ್ನಡದ ಜಾಯಮಾನಕ್ಕೆ ಹೊಂದುವ ಭಾಷೆಯಲ್ಲಿ ಅನುವಾದಿಸಿ ಕನ್ನಡಿಗರಿಗೆ ರಾಮರಸಾಯನ ಉಣಬಡಿಸಿದರು. ಇಷ್ಟಲ್ಲದೆ ಕನ್ನಡ ಸಾಹಿತ್ಯಪರಿಷತ್ತಿನ ಹಳೆಗನ್ನಡ ಗದ್ಯಾನುವಾದ ಸಮಿತಿಯ ಪ್ರಧಾನ ಸಂಪಾದಕರಾಗಿ ಸುಮಾರು ೧೩ ಗ್ರಂಥಗಳನ್ನು ಹೊಸಗನ್ನಡಕ್ಕೆ ಅನುವಾದಿಸಿದರು. ಅನೇಕ ದಿನಪತ್ರಿಕೆ, ವಾರಪತ್ರಿಕೆ, ಮಾಸಪತ್ರಿಕೆಗಳಲ್ಲಿ ಲೇಖನಗಳನ್ನು ಬರೆಯುವ ಮೂಲಕ ನಿರಂತರವಾಗಿ ಸ್ವಚಿಂತನೆಗಳನ್ನು ಸಮಾಜಕ್ಕೆ ನೀಡುತ್ತಿದ್ದರು.ಸಂಸ್ಕೃತ ಲೇಖನಗಳು, ನಾಟಕಗಳು, ಸ್ತೋತ್ರಗಳು, ಕನ್ನಡಗ್ರಂಥಗಳು, ಅನುವಾದ ಗ್ರಂಥಗಳು ಹೀಗೆ ಸಂಸ್ಕೃತ ಕನ್ನಡ ಸಾಹಿತ್ಯ ಕ್ಷೇತ್ರದಲ್ಲಿ ಸುಮಾರು ೯೦ ಕ್ಕೂ ಹೆಚ್ಚು ಗ್ರಂಥಗಳನ್ನು ರಚಿಸಿ ಅಪಾರವಾದ ಸೇವೆಯನ್ನು ಸಲ್ಲಿಸಿ ಜೀವನವನ್ನು ಸಾರ್ಥಕಗೊಳಿಸಿದರು. ದೇಶದಲ್ಲಿ ನಡೆಯುತ್ತಿದ್ದ ಪ್ರತಿಷ್ಠಿತ ವಿದ್ವದ್ಗೋಷ್ಠಿಗಳಲ್ಲಿ ಭಾಗವಹಿಸಿ ಸದಾ ಸರಸ್ವತೀ ಸೇವೆಯನ್ನು ಮಾಡುತ್ತಿದ್ದರು. ಯಾವುದೇ ಜಾತಿ ಮತ ಪಂಥಗಳ ಭೇದಗಳಿಲ್ಲದೆ ಎಲ್ಲಾ ಮಠಗಳಲ್ಲಿ ನಡೆಯುತ್ತಿದ್ದ ಗೋಷ್ಠಿಗಳಲ್ಲಿ ಭಾಗವಹಿಸುತ್ತಿದ್ದರು.</p>
						<h4>ಪ್ರಶಸ್ತಿಗಳು</h4>
						<p>ಶ್ರೀಯುತ ಶರ್ಮರ ಗುಣ, ಪಾಂಡಿತ್ಯ, ಪ್ರತಿಭೆಯನ್ನು ಗುರುತಿಸಿ ಅನೇಕ ಮಠಗಳು, ಸಂಸ್ಥೆಗಳು, ವಿಶ್ವವಿದ್ಯಾಲಯಗಳು ಇವರನ್ನು ಗೌರವಿಸುವ ಮೂಲಕ ತಮ್ಮ ಗೌರವವನ್ನು ಹೆಚ್ಚಿಸಿಕೊಂಡಿವೆ. ನಡಹಳ್ಳಿಯ ಗ್ರಾಮಸ್ಥರ ಪ್ರೀತಿಯ ಸಮ್ಮಾನ ಕಾರ್ಯಕ್ರಮದಿಂದಾರಂಭಿಸಿ ರಾಷ್ಟ್ರಪತಿ ಪುರಸ್ಕಾರದವರೆಗಿನ ಎಲ್ಲಾ ಪ್ರಶಸ್ತಿಗಳೂ ಇವರಿಗೆ ಸಂದಿವೆ.</p>
