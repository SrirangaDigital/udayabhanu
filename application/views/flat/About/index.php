						<h4 class="mt-4">Student life</h4>
						<p>Born into a family of farmers, Sri Sharma had his primary education in Nadahalli and Soraba. Then, he started his Sanskrit studies in the Agadi Anandashrama, but had to discontinue within a short time because of ill health.  After that, for about three years, he studied Veda and basic Samskrita from Matturu Vedabrahma Sri Narayana Shastri, at the pathashala in the Rameshvara temple of Keladi. He passed the Prathamaa and Kaavya exams, and joined Sri Chamarajendra Samskrita Mahapathashala, Bangalore, for studying Sahitya. After completing Sahitya, he studied the advanced texts of Vyakarana under famed scholars like Vedabrahma Sri. Nuggehalli Tirunarayana Ayyangar, Vid. Timmanacharya, Vid. Srikantha Shastri and Vid. Krishna Shastri. He passed the Vidvan-madhyamaa and Vidvad-uttamaa examinations in distinction, and was awarded the degree of Vyaakarana Vidvan.</p>
						<p>Further, he studied the texts of Alankaara Shastra under Vedabrahma Sri. Lakshminarayana Shastri and Panditapravara Koduru Krishna Jois, and passed the examination for Vidvat in Alankara Shastra. He also studied Vedanta under the same two scholars, and also learnt the advanced texts of Vedanta in depth from the great scholar and Srividya upaasaka, Vedabrahma Sri. K.P. Shankara Shastri, to become a Vedanta Vidvan. Sri Sharma was devoted to studies, and he also learnt the various modifications of the Kannada language traditionally (Halagannada, Nadugannada and Hosagannada), and passed the Kannada Vidvan examination conducted by the Madras University with distinction. He also passed the examination conducted by the Karnataka Government, to gain the title of Kannada Pandita.</p>
						<p>Sri Sharma was the secretary of Students’ union of Sri Chamarajendra Sanskrit College for four years. He also served as the organizer of students in the Vyasamandira Student Hostel where he lived.</p>
						<h4>Professional Career</h4>
						<p>Sri. Sharma started his professional career as a teacher in a highschool in Hassan. He then joined the Samskrita Pathashala at Belur as a Sanskrit teacher. In 1948, he was appointed as a lecturer in the Department of Vyakarana at Sri. Chamarajendra Samskrita College in Bangalore. Having taught there for nearly 28 years, he retired in 1976. Through his career, he taught hundreds of students both within and outside the college. He was especially fond of two of his best students – Mahamahopadhyaya Vid. G Mahabaleshwar Bhat and Mahamahopadhyaya Vid. So Ti Nagaraja. He was proud of the mastery they had gained in Vyakarana through hard work. He always said that Mahabaleshwar Bhat and So Ti Nagaraja were like his two eyes. He was extremely happy that these two, having learnt the shastra from him, were in turn passing it on to deserving students, and nurturing the next generation of scholars. Delighted at seeing the family of his student-lineage growing, he enthused them with his appreciation and encouragement.</p>
