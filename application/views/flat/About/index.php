						<h4 class="mt-4">ವಿದ್ಯಾರ್ಥಿ ಜೀವನ</h4>
						<p>ಕೃಷಿಕ ಕುಟುಂಬದ ಹಿನ್ನೆಲೆಯಿಂದ ಬಂದ ಇವರ ಪ್ರಾಥಮಿಕ ವಿದ್ಯಾಭ್ಯಾಸವು ನಡಹಳ್ಳಿ ಮತ್ತು ಸೊರಬಗಳಲ್ಲಿ ನಡೆಯಿತು. ಮುಂದೆ ಅಗಡಿ ಆನಂದಾಶ್ರಮದಲ್ಲಿ ಸಂಸ್ಕೃತಾಧ್ಯಯನವನ್ನು ಆರಂಭಿಸಿದರೂ ಅನಾರೋಗ್ಯದ ಕಾರಣದಿಂದ ಅಲ್ಪಕಾಲದಲ್ಲೇ ಅಲ್ಲಿಂದ ಬಿಡಬೇಕಾಯಿತು. ಅನಂತರ ಕೆಳದಿಯ ರಾಮೇಶ್ವರ ದೇವಾಲಯದಲ್ಲಿ ನಡೆಯುತ್ತಿದ್ದ ಸಂಸ್ಕೃತ ಪಾಠಶಾಲೆಯಲ್ಲಿ ಸುಮಾರು ಮೂರು ವರ್ಷಗಳ ಕಾಲ ವೇದ ಮತ್ತು ಸಂಸ್ಕೃತಗಳ ಬಾಲಪಾಠಗಳನ್ನು ಮತ್ತೂರು ವೇ. ಬ್ರ. ಶ್ರೀ. ನಾರಾಯಣ ಶಾಸ್ತ್ರಿಗಳಲ್ಲಿ ಅಧ್ಯಯನ ಮಾಡಿದರು. ಪ್ರಥಮಕಾವ್ಯಗಳನ್ನು ಪೂರೈಸಿ ಸಾಹಿತ್ಯಾಧ್ಯಯನಕ್ಕಾಗಿ ಬೆಂಗಳೂರಿನ ಶ್ರೀ ಚಾಮರಾಜೇಂದ್ರ ಸಂಸ್ಕೃತ ಮಹಾಪಾಠಶಾಲೆಗೆ ಸೇರಿದರು. ಇಲ್ಲಿ ಸಾಹಿತ್ಯಾಧ್ಯಯನದ ನಂತರ ಸುಪ್ರಸಿದ್ಧ ವಿದ್ವಾಂಸರಾದ ವೇ.ಬ್ರ. ಶ್ರೀ. ನುಗ್ಗೇಹಳ್ಳಿ ತಿರುನಾರಾಯಣ ಅಯ್ಯಂಗಾರ್, ವಿದ್ವಾನ್ ತಿಮ್ಮಣ್ಣಾಚಾರ್ಯರು, ವಿದ್ವಾನ್ ಶ್ರೀಕಂಠಶಾಸ್ತ್ರಿ, ವಿದ್ವಾನ್ ಕೃಷ್ಣಶಾಸ್ತ್ರಿಗಳಲ್ಲಿ ವ್ಯಾಕರಣ ಶಾಸ್ತ್ರದ ಪ್ರೌಢಗ್ರಂಥಗಳನ್ನು ಅಧ್ಯಯನ ಮಾಡಿ ವ್ಯಾಕರಣ ವಿದ್ವನ್ಮಧ್ಯಮಾ, ವಿದ್ವದುತ್ತಮಾಗಳನ್ನು ವೈಶಿಷ್ಟ್ಯ ಶ್ರೇಣಿಗಳಲ್ಲಿ ತೇರ್ಗಡೆ ಹೊಂದಿ ವ್ಯಾಕರಣ ವಿದ್ವಾನ್ ಎಂಬ ಪದವಿಯನ್ನು ಹೊಂದಿದರು.</p>
						<p>ಅನಂತರ ವೇ. ಬ್ರ. ಶ್ರೀ. ಲಕ್ಷ್ಮೀನಾರಾಯಣ ಶಾಸ್ತ್ರಿ ಹಾಗೂ ಪಂಡಿತ ಪ್ರವರ ಕೋಡೂರು ಕೃಷ್ಣಜೋಯಿಸರಲ್ಲಿ ಅಲಂಕಾರಶಾಸ್ತ್ರದ ಪ್ರೌಢಗ್ರಂಥಗಳನ್ನು ಅಧ್ಯಯನ ಮಾಡಿ ಪರೀಕ್ಷೆಯಲ್ಲಿ ತೇರ್ಗಡೆ ಹೊಂದಿ ಅಲಂಕಾರ ವಿದ್ವಾನ್ ಎಂಬ ಪದವಿಯನ್ನು ಹೊಂದಿದರು. ಇಷ್ಟಲ್ಲದೆ ಈ ಈರ್ವರು ವಿದ್ವಾಂಸರಲ್ಲಿಯೇ ಅದ್ವೈತ ವೇದಾಂತ ಶಾಸ್ತ್ರವನ್ನು ಅಧ್ಯಯನ ಮಾಡಿ ವೇದಾಂತಶಾಸ್ತ್ರಪಾರಂಗತರೂ ಶ್ರೀವಿದ್ಯೋಪಾಸಕರೂ ಆದ ವೇ. ಬ್ರ. ಶ್ರೀ. ಕೆ. ಪಿ. ಶಂಕರಶಾಸ್ತ್ರಿಗಳಲ್ಲಿ ಅದ್ವೈತ ವೇದಾಂತದ ಪ್ರೌಢಗ್ರಂಥಗಳನ್ನು ಆಳವಾಗಿ ಅಭ್ಯಸಿಸಿ ವೇದಾಂತ ವಿದ್ವಾನ್ ಎನಿಸಿಕೊಂಡರು. ನಿರಂತರ ಅಧ್ಯಯನ ಶೀಲರಾದ ಇವರ ಜ್ಞಾನದಾಹ ಇಲ್ಲಿಗೇ ತೀರದೆ ಹಳೆಗನ್ನಡ –ನಡುಗನ್ನಡ-ಹೊಸಗನ್ನಡಗಳನ್ನು ಶಾಸ್ತ್ರೀಯವಾಗಿ ಅಧ್ಯಯನ ಮಾಡಿ ಮದ್ರಾಸ್ ವಿಶ್ವವಿದ್ಯಾಲಯ ನಡೆಸುತ್ತಿದ್ದ ಕನ್ನಡ ವಿದ್ವಾನ್ ಪರೀಕ್ಷೆಗೆ ಕುಳಿತು ವಿಶಿಷ್ಟ ಶ್ರೇಣಿಯಲ್ಲಿ ತೇರ್ಗಡೆ ಹೊಂದಿ ಕನ್ನಡ ವಿದ್ವಾನ್ ಎಂಬ ಪದವಿಯನ್ನೂ ತಮ್ಮದಾಗಿಸಿಕೊಂಡರು. ಕರ್ನಾಟಕ ಸರಕಾರ ನಡೆಸುತ್ತಿದ್ದ ಕನ್ನಡ ಪಂಡಿತ ಪರೀಕ್ಷೆಯಲ್ಲೂ ತೇರ್ಗಡೆ ಹೊಂದಿ ಕರ್ನಾಟಕ ಸರಕಾರ ನೀಡುವ ಕನ್ನಡ ಪಂಡಿತ ಎಂಬ ಬಿರುದಿಗೆ ಪಾತ್ರರಾದರು.</p>
						<p>ಶ್ರೀಯುತ ಶರ್ಮರು ಶ್ರೀಚಾಮರಾಜೇಂದ್ರ ಸಂಸ್ಕೃತ ಕಾಲೇಜಿನ ವಿದ್ಯಾರ್ಥಿಸಂಘಕ್ಕೆ ನಾಲ್ಕು ವರ್ಷಗಳ ಕಾಲ ಕಾರ್ಯದರ್ಶಿಯಾಗಿದ್ದರು. ಅಲ್ಲದೆ ತಾವು ವಾಸವಾಗಿದ್ದ ವ್ಯಾಸಮಂದಿರ ವಿದ್ಯಾರ್ಥಿನಿಲಯದಲ್ಲಿ ವಿದ್ಯಾರ್ಥಿಗಳ ವ್ಯವಸ್ಥಾಪಕರಾಗಿಯೂ ಕಾರ್ಯನಿರ್ವಹಿಸಿದ್ದರು.</p>
						<h4>ವೃತ್ತಿಜೀವನ</h4>
						<p>ಹಾಸನ ಜಿಲ್ಲೆಯ ಪ್ರೌಢಶಾಲೆಯೊಂದರಲ್ಲಿ ಸಂಸ್ಕೃತಾಧ್ಯಾಪಕರಾಗಿ ವೃತ್ತಿಜೀವನ ಆರಂಭಿಸಿದ ಶ್ರೀಯುತರು ಮುಂದೆ ಬೇಲೂರಿನ ಸಂಸ್ಕೃತಪಾಠಶಾಲೆಯಲ್ಲಿ ಸಂಸ್ಕೃತಾಧ್ಯಾಪಕರಾಗಿ ಸೇರಿದರು. ೧೯೪೮ರಲ್ಲಿ ಬೆಂಗಳೂರಿನ ಶ್ರೀಚಾಮರಾಜೇಂದ್ರ ಸಂಸ್ಕೃತಕಾಲೇಜಿನಲ್ಲಿ ವ್ಯಾಕರಣ ಶಾಸ್ತ್ರ ವಿಭಾಗದಲ್ಲಿ ಅಧ್ಯಾಪಕರಾಗಿ ನಿಯುಕ್ತರಾದರು. ಸುಮಾರು ೨೮ ವರ್ಷಗಳ ಕಾಲ ಅಧ್ಯಾಪನ ಮಾಡಿ ೧೯೭೬ರಲ್ಲಿ ನಿವೃತ್ತರಾದರು. ತನ್ನ ಬದುಕಿನುದ್ದಕ್ಕೂ ನೂರಾರು ಶಿಷ್ಯರಿಗೆ ಪಾಠಶಾಲೆಯ ಒಳಗೆ ಹಾಗೂ ಹೊರಗೆ ಪಾಠಹೇಳಿದ್ದರೂ ಮಹಾಮಹೋಪಾಧ್ಯಾಯ ವಿದ್ವಾನ್ ಜಿ ಮಹಾಬಲೇಶ್ವರ ಭಟ್ ಹಾಗೂ ಮಹಾಮಹೋಪಾಧ್ಯಾಯ ವಿದ್ವಾನ್ ಸೋ. ತಿ .ನಾಗರಾಜರು ಇವರಿಬ್ಬರ ವಿಚಾರದಲ್ಲಿ ವಿಶೇಷವಾದ ಪ್ರೀತಿ ಅಭಿಮಾನವಿತ್ತು. ಇವರೀರ್ವರ ಅಧ್ಯಯನ ಶ್ರಿಯುತರ ಮನಸ್ಸಿಗೆ ಬಹಳ ಸಮಾಧಾನವನ್ನು ತಂದೊಡ್ಡಿತ್ತು. ಆದ್ದರಿಂದಲೇ ಮಹಾಬಲೇಶ್ವರ ಭಟ್ಟ ಮತ್ತು ನಾಗರಾಜರು ನನ್ನೆರಡು ಕಣ್ಣುಗಳು ಎಂದು ಸದಾಕಾಲ ಹೇಳುತ್ತಿದ್ದರು. ತನ್ನ ಗುರುಗಳಿಂದ ಕಲಿತ ವ್ಯಾಕರಣಶಾಸ್ತ್ರವನ್ನು ಇವರಿಬ್ಬರೂ ಗುರೂಪದೇಶದಂತೆ ಶಿಷ್ಯರಿಗೆ ಬೋಧಿಸಿ ವ್ಯುತ್ಪನ್ನ ಶಾಸ್ತ್ರಜ್ಞರನ್ನು ಸಿದ್ಧಗೊಳಿಸಿ ಜಗಕೆ ನೀಡಿದ ಶಿಷ್ಯೋತ್ತಮರಾಗಿದ್ದರಿಂದ ಇವರೀರ್ವರ ಮೆಲೆ ವಿಶೇಷವಾದ ಅಭಿಮಾನ ಶರ್ಮರಿಗಿತ್ತು. ತನ್ನ ವಿದ್ಯಾವಂಶ ಬೆಳೆಯುತ್ತಿರುವುದನ್ನು ನೋಡಿ, ಬೆಳೆಸುತ್ತಿರುವ ಶಿಷ್ಯರನ್ನು ಎಲ್ಲೆಡೆ ಅಭಿನಂದಿಸುತ್ತಿದ್ದರು.</p>
